export type PostsQuery = (
  { __typename?: 'Query' }
  & { posts: (
    { __typename?: 'PaginatedPosts' }
    & Pick<PaginatedPosts, 'hasMore'>
    & { posts: Array<(
      { __typename?: 'Post' }
      & { creator: (
        { __typename?: 'User' }
        & Pick<User, 'username' | 'id'>
        & { goblinMask: (
          { __typename?: 'GoblinMask' }
          & Pick<GoblinMask, 'id'>
        ) }
      ) }
      & PostSnippetFragment
    )> }
  ) }
);

